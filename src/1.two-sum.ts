// @lc code=start
function twoSum(nums: number[], target: number): number[] {
  for (let i = 0, len = nums.length; i < len; i++) {
    for (let j = i + 1; j < len; j++) {
      if (nums[i] + nums[j] === target) {
        return [i, j];
      }
    }
  }
  throw new Error("Target not found in the array.");
}
// @lc code=end

export { twoSum };